export function formatUiMessage(
  locale: UiLocale,
  key: string,
  params?: Record<string, string>
): string {
  let s = UI_MESSAGES[locale]?.[key] ?? UI_MESSAGES.ru[key] ?? key
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      s = s.replaceAll(`{${k}}`, v)
    }
  }
  return s
}

export function readStoredUiLocale(): UiLocale {
  if (typeof window === "undefined") return "ru"
  const v = window.localStorage.getItem(UI_LANG_STORAGE_KEY)
  if (v === "en" || v === "zh") return v
  return "ru"
}

export function localeToHtmlLang(locale: UiLocale): string {
  return locale === "zh" ? "zh" : locale
}

export function localeToBcp47(locale: UiLocale): string {
  if (locale === "zh") return "zh-CN"
  if (locale === "en") return "en-US"
  return "ru-RU"
}
